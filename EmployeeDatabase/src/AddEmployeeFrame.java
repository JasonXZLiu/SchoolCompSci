    public int encodeWorkLoc(String s) {
        int i = encodeCities.size();
        if(cities.contains(s)) return encodeCities.get(s);
        else {
            encodeCities.put(s, i);
            cities.add(s);
            return encodeCities.size();
        }
    }
    
